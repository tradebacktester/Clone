// ─── Research History ─────────────────────────────────────────────────────────
// Full reproducible audit log of all research events.

export const rlHistoryTable = pgTable("rl_history", {
  historyId:    uuid("history_id").primaryKey().defaultRandom(),
  eventType:    text("event_type").notNull(),  // project_created | hypothesis_generated | experiment_started | validation_passed | validation_failed | comparison_complete | recommendation_created | approval_requested | approved | rejected | deployed | rolled_back
  entityType:   text("entity_type").notNull(), // project | hypothesis | experiment | comparison | recommendation | approval
  entityId:     uuid("entity_id").notNull(),
  projectId:    uuid("project_id"),
  title:        text("title").notNull(),
  description:  text("description").notNull(),
  metadata:     jsonb("metadata").$type<Record<string, unknown>>(),
  isReproducible: boolean("is_reproducible").notNull().default(true),
  createdAt:    timestamp("created_at").notNull().defaultNow(),
}, t => [
  index("rl_history_event_idx").on(t.eventType),
  index("rl_history_entity_idx").on(t.entityId),
]);
